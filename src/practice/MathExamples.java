package practice;

public class MathExamples {

	public static void main(String[] args) {
		
		System.out.println(Math.abs(-26.7)); //number from zero
		System.out.println(Math.ceil(7.4)); //rounds up
		System.out.println(Math.floor(7.8)); //rounds down
		System.out.println(Math.max(8.6, 5.2)); //gives maximum of the two numbers
		System.out.println(Math.min(8.6, 5.2)); // gives minimum of the two numbers
		System.out.println(Math.pow(5,3)); //to the power. 
		System.out.println(Math.sqrt(9)); //square  root
		
	}
}

